export function getWuxingTopicAnalysis(wuxingElement: string, topicId: string): string {
  const analysis = WUXING_TOPIC[wuxingElement];
  if (!analysis) return "";
  const map: Record<string, string> = {
    love: analysis.love,
    career: analysis.career,
    wealth: analysis.wealth,
    health: analysis.health,
    social: analysis.social,
  };
  return map[topicId] || "";
}

